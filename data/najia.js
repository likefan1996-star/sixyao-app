/**
 * 纳甲规则：八纯卦各爻的天干地支配对（京房纳甲体系）
 *
 * 每条记录对应一个三爻卦（八卦之一），分为内卦（下三爻）和外卦（上三爻）
 * 每爻赋值格式：{ stem, branch, stemIndex, branchIndex }
 *
 * 八宫所属：
 *   乾金宫（纳甲）：内甲子→甲寅→甲辰，外甲午→甲申→甲戌
 *   坎水宫（纳戊）：内戊寅→戊辰→戊午，外戊申→戊戌→戊子
 *   艮土宫（纳丙）：内丙辰→丙午→丙申，外丙戌→丙子→丙寅
 *   震木宫（纳庚）：内庚子→庚寅→庚辰，外庚午→庚申→庚戌
 *   巽木宫（纳辛）：内辛丑→辛亥→辛酉，外辛未→辛巳→辛卯
 *   离火宫（纳己）：内己卯→己丑→己亥，外己酉→己未→己巳
 *   坤土宫（纳乙）：内乙未→乙巳→乙卯，外乙丑→乙亥→乙酉
 *   兑金宫（纳丁）：内丁巳→丁卯→丁丑，外丁亥→丁酉→丁未
 */

const STEMS = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸']
const BRANCHES = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥']

function make(stem, branch) {
  return {
    stem,
    branch,
    stemIndex: STEMS.indexOf(stem),
    branchIndex: BRANCHES.indexOf(branch)
  }
}

